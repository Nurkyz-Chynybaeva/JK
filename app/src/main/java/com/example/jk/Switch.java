package com.example.jk;

public class Switch {

    public static void main(String[] args) {
        int x = 1;
        switch (x) {
            case 1:
                System.out.println("first");
                break;
            case 2:
                System.out.println("second");
                break;
            case 3:
                System.out.println("thrid");
            default:
                System.out.println("another number");
        }
    }
}

